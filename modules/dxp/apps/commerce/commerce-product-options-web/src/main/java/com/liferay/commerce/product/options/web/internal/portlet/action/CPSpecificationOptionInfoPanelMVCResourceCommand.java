package com.liferay.commerce.product.options.web.internal.portlet.action;

import com.liferay.commerce.product.constants.CPPortletKeys;
import com.liferay.commerce.product.constants.CPWebKeys;
import com.liferay.commerce.product.model.CPSpecificationOption;
import com.liferay.portal.kernel.portlet.bridges.mvc.BaseMVCResourceCommand;
import com.liferay.portal.kernel.portlet.bridges.mvc.MVCResourceCommand;

import java.util.List;

import javax.portlet.ResourceRequest;
import javax.portlet.ResourceResponse;

import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Andrea Di Giorgi
 */
@Component(
	immediate = true,
	property = {
		"javax.portlet.name=" + CPPortletKeys.CP_SPECIFICATION_OPTIONS,
		"mvc.command.name=cpSpecificationOptionInfoPanel"
	},
	service = MVCResourceCommand.class
)
public class CPSpecificationOptionInfoPanelMVCResourceCommand
	extends BaseMVCResourceCommand {

	@Override
	protected void doServeResource(
			ResourceRequest resourceRequest, ResourceResponse resourceResponse)
		throws Exception {

		List<CPSpecificationOption> cpSpecificationOptions =
			_actionHelper.getCPSpecificationOptions(resourceRequest);

		resourceRequest.setAttribute(
			CPWebKeys.CP_SPECIFICATION_OPTIONS, cpSpecificationOptions);

		include(
			resourceRequest, resourceResponse,
			"/specification_option_info_panel.jsp");
	}

	@Reference
	private ActionHelper _actionHelper;

}